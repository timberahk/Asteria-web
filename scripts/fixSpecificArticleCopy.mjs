function findObjectRange(text, id) {
  const idPattern = new RegExp(`\\n\\s*\\{\\n\\s*"id":\\s*${id},`);
  const match = idPattern.exec(text);
  if (!match) throw new Error(`Article ${id} not found`);
  const start = match.index + 1;
  let depth = 0;
  let inString = false;
  let escaped = false;
  for (let i = start; i < text.length; i += 1) {
    const char = text[i];
    if (inString) {
      if (escaped) {
        escaped = false;
      } else if (char === '\\') {
        escaped = true;
      } else if (char === '"') {
        inString = false;
      }
      continue;
    }
    if (char === '"') inString = true;
    if (char === '{') depth += 1;
    if (char === '}') {
      depth -= 1;
      if (depth === 0) return [start, i + 1];
    }
  }
  throw new Error(`Article ${id} object did not close`);
}

function replaceStringProperty(objectText, property, value) {
  const propertyPattern = new RegExp(`("${property}"\\s*:\\s*)"(?:\\\\.|[^"\\\\])*"`);
  if (!propertyPattern.test(objectText)) throw new Error(`Property ${property} not found`);
  return objectText.replace(propertyPattern, `$1${JSON.stringify(value)}`);
}

