function specialRecipeFor(post, topic) {
  const haystack = `${post.title} ${post.summary} ${post.category}`;
  const matched = specialRecipes.find((recipe) => recipe.test.test(haystack));
  return matched ? matched.build(topic) : null;
}

function buildContent(post) {
  const cleanTitle = stripBrackets(post.title);
  const topic = normalizeTopic(cleanTitle);
  const recipe = specialRecipeFor(post, topic) || recipes[intentFor(post)](topic);
  const intro = paragraph(recipe.intro);
  const sections = recipe.sections.map(([heading, body]) => `<h2>${heading}</h2>\n<p>${body}</p>`);
  const faq = [
    '<h2>常見問題</h2>',
    ...recipe.faqs.map(([question, answer]) => `<h3>${question}</h3>\n<p>${answer}</p>`),
  ];
  return [`<p>${intro}</p>`, ...sections, ...faq].join('\n');
}

