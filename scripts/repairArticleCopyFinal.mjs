function extractPosts(src) {
  const marker = 'export const teachingPosts';
  const startMarker = src.indexOf(marker);
  if (startMarker === -1) throw new Error('Cannot find teachingPosts export.');
  const assignment = src.indexOf('=', startMarker);
  const arrayStart = src.indexOf('[', assignment);
  let depth = 0;
  let inString = false;
  let quote = '';
  let escaped = false;
  for (let i = arrayStart; i < src.length; i += 1) {
    const char = src[i];
    if (inString) {
      if (escaped) escaped = false;
      else if (char === '\\') escaped = true;
      else if (char === quote) inString = false;
      continue;
    }
    if (char === '"' || char === "'" || char === '`') {
      inString = true;
      quote = char;
      continue;
    }
    if (char === '[') depth += 1;
    if (char === ']') depth -= 1;
    if (depth === 0) {
      return Function(`"use strict"; return (${src.slice(arrayStart, i + 1)});`)();
    }
  }
  throw new Error('Cannot parse teachingPosts array.');
}

function escapeTsString(value) {
  return value
    .replace(/\\/g, '\\\\')
    .replace(/"/g, '\\"')
    .replace(/\n/g, '\\n');
}

function stripBrackets(title) {
  return title
    .replace(/[【】\[\]]/g, '')
    .replace(/\s*AI\s*摘要.*$/i, '')
    .replace(/\s+/g, ' ')
    .trim();
}

function shortTopic(title) {
  const clean = stripBrackets(title)
    .replace(/[？?].*$/, '')
    .replace(/[：:].*$/, '')
    .replace(/^\d+\s*個/, '')
    .trim();
  return clean.length > 18 ? clean.slice(0, 18) : clean;
}

function normalizeTopic(title) {
  const clean = shortTopic(title)
    .replace(/^為什麼/, '')
    .replace(/^點解/, '')
    .replace(/^如何/, '')
    .replace(/^怎樣/, '')
    .replace(/^點樣/, '')
    .replace(/^原來/, '')
    .trim();
  return clean || shortTopic(title);
}

function intentFor(post) {
  const title = `${post.title} ${post.summary} ${post.category}`;
  if (/復合|挽回|分手|前任|回頭|返嚟|假性分手|重修舊好/.test(title)) return 'reconnect';
  if (/斷聯|冷淡|少覆|已讀|不讀|唔覆|失聯|封鎖|unblock|block|覆訊息|message/i.test(title)) return 'distance';
  if (/第三者|新歡|出軌|介入|曖昧對象|桃花|暗戀|單身|脫單/.test(title)) return 'triangle';
  if (/溝通|勸|說話|訊息|聊天|嗌交|吵架|講道理|T 人|直男|哄|氹|相處|冷暴力/.test(title)) return 'communication';
  if (/儀式|塔羅|占卜|能量|白魔法|黑魔法|法術|牌陣/.test(title)) return 'ritual';
  if (/焦慮|不安|安全感|委屈|自卑|內耗|心動|依賴|放低|情緒|失戀|壓力|成長/.test(title)) return 'emotion';
  if (/Long D|遠距離/i.test(title)) return 'distance';
  return 'relationship';
}

