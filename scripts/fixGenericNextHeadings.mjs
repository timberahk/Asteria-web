import fs from "fs";

const file = "lib/articlesData.ts";
let source = fs.readFileSync(file, "utf8");

const headingFor = (focus) => {
  if (/勸不動|不說|不懂|批評|溝通|反駁|起鋼|需求|解釋|吵架|燥底|呻|道歉|答應/.test(focus)) {
    return "把重點收窄成對方聽得入耳的一句";
  }
  if (/情緒|情傷|佔有|患得患失|呷醋|安全感|妒忌|巨嬰/.test(focus)) {
    return "先穩住情緒，再決定要怎樣靠近";
  }
  if (/分享|親密|唔上心|孤獨|升溫|長久|坦白/.test(focus)) {
    return "把親密感落回日常互動";
  }
  if (/同居|見家長/.test(focus)) {
    return "把期待講清楚，才不會日後靠猜";
  }
  if (/男人|離不開|重視|寵壞/.test(focus)) {
    return "先看懂對方需要，再調整相處節奏";
  }
  if (/大男人/.test(focus)) {
    return "先給對方面子，再慢慢推動改變";
  }
  if (/朋友/.test(focus)) {
    return "先分清外界聲音和對方真正立場";
  }
  return "下一步先整理重點，再決定怎樣開口";
};

let changed = 0;
source = source.replace(/<h2>下一步：把「([^」]+)」講成可被接住的需要<\/h2>/g, (_match, focus) => {
  changed += 1;
  return `<h2>${headingFor(focus)}</h2>`;
});

