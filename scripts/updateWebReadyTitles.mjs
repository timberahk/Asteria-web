const yamlQuote = (value) => JSON.stringify(value);

const files = fs.readdirSync(webReadyDir)
  .filter((file) => /^\d{3}.*\.md$/.test(file))
  .sort((a, b) => a.localeCompare(b, 'zh-Hant'));

let changed = 0;

for (const file of files) {
  const id = file.match(/^(\d{3})/)?.[1];
  const headline = titles[id];
  if (!headline) continue;

  const fullTitle = `${headline}${brandSuffix}`;
  const filePath = path.join(webReadyDir, file);
  const original = fs.readFileSync(filePath, 'utf8');
  let next = original;

  if (/^---\n[\s\S]*?\n---\n/.test(next)) {
    if (/^seo_title:\s*.*$/m.test(next)) {
      next = next.replace(/^seo_title:\s*.*$/m, `seo_title: ${yamlQuote(fullTitle)}`);
    } else {
      next = next.replace(/^---\n/, `---\nseo_title: ${yamlQuote(fullTitle)}\n`);
    }
  } else {
    next = `---\nseo_title: ${yamlQuote(fullTitle)}\n---\n\n${next}`;
  }

  next = next.replace(/(^|\n)# .*(?=\n)/, `$1# ${headline}`);

  if (next !== original) {
    fs.writeFileSync(filePath, next);
    changed += 1;
  }
}

console.log(`Updated ${changed} Web ready title files.`);
