function splitFaq(content) {
  const faqIndex = content.indexOf("<h2>常見問題</h2>");
  if (faqIndex === -1) return "";
  return content.slice(faqIndex);
}

let changed = 0;
for (const post of posts) {
  const body = replacements.get(post.id);
  if (!body) continue;
  const faq = splitFaq(post.content);
  post.content = faq ? `${body}\n${faq}` : body;
  changed += 1;
}

if (changed !== replacements.size) {
  throw new Error(`Expected ${replacements.size} replacements, wrote ${changed}`);
}

