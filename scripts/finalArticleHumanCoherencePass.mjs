function fixGenericHeadings(post) {
  const combined = `${post.title} ${post.summary} ${post.content}`;
  const pool = headingPools.find((entry) => entry.test.test(combined));
  if (!pool) return post.content;
  let index = 0;
  return post.content.replace(/<h2>(.*?)<\/h2>/g, (match, heading) => {
    if (heading === "常見問題") return match;
    if (!bannedHeadings.includes(heading) && !/^重點\d+/.test(heading) && !/可以點做/.test(heading)) {
      return match;
    }
    const nextHeading = pool.headings[index % pool.headings.length];
    index += 1;
    return `<h2>${nextHeading}</h2>`;
  });
}

function cleanTitleText(text) {
  return text
    .replace(/可以點做？/g, "？")
    .replace(/可以點做/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

