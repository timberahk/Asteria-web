for (const post of posts) {
  if (titleUpdates.has(post.id)) post.title = titleUpdates.get(post.id);
  if (!additions.has(post.id)) continue;
  const [body, faq] = post.content.split(/<h2[^>]*>\s*常見問題\s*<\/h2>/i);
  post.content = `${body.trim()}\n${additions.get(post.id).join('\n')}\n<h2>常見問題</h2>${faq}`;
}

const serialized = JSON.stringify(posts, null, 2)
  .replace(/"([^"]+)":/g, '$1:')
  .replace(/\\u003c/g, '<')
  .replace(/\\u003e/g, '>')
  .replace(/\\u0026/g, '&');

fs.writeFileSync(articlesFile, `${source.slice(0, arrayStart)}${serialized}${source.slice(arrayEnd)}`);
console.log(`polished ${additions.size} short articles`);
